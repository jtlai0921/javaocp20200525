package model;

public class orderline {
private int orderid;
private int foodid;
private int qty;
public int getOrderid() {
	return orderid;
}
public void setOrderid(int orderid) {
	this.orderid = orderid;
}
public int getFoodid() {
	return foodid;
}
public void setFoodid(int foodid) {
	this.foodid = foodid;
}
public int getQty() {
	return qty;
}
public void setQty(int qty) {
	this.qty = qty;
}

}
