package model;

public class food {
private String foodname;
private int price;
private String img;
public String getImg() {
	return img;
}
public void setImg(String img) {
	this.img = img;
}
public String getFoodname() {
	return foodname;
}
public void setFoodname(String foodname) {
	this.foodname = foodname;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}

}
